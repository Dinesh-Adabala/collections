public class StudentArray {
    String name;
    int Idno;

    public String getName() {
        return name;
    }

    public int getIdno() {
        return Idno;
    }


    public void setName(String name) {
        this.name = name;
    }

    public void setIdno(int idno) {
        Idno = idno;
    }

    public StudentArray(String name, int Idno) {
        this.name = name;
        this.Idno = Idno;
    }

}
